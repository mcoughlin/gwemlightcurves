def get_BNSKilonovaLightcurve_model(table, **kwargs):
    table['mej'] = calc_meje(table['m1'], table['mb1'], table['c1'], table['m2'], table['mb2'], table['c2'])
    # Throw out smaples where the mass ejecta is less than zero.
    mask = (table['mej'] > 0)
    table = table[mask]
    # Log mass ejecta
    table['mej'] = np.log10(table['mej'])
    # calc the velocity of ejecta for those non-zero ejecta mass samples
    table['vej'] = calc_vej(table['m1'],table['c1'],table['m2'],table['c2'])
    table['t'], table['lbol'], table['mag'] = calc_lc(table['tini'],table['tmax'],
                                                      table['dt'],table['mej'],table['vej'],table['vmin'],
                                                      table['th'],table['ph'],itable['kappa'],
                                                      table['eps'],table['alp'],table['eth'],table['flgbct'])
    return table

def calc_meje(m1,mb1,c1,m2,mb2,c2):

    a= -1.35695
    b=  6.11252 
    c=-49.43355      
    d=  16.1144    
    n=  -2.5484

    tmp1=((mb1*((m2/m1)**(1.0/3.0))*(1.0-2.0*c1)/c1)+(mb2*((m1/m2)**(1.0/3.0))*(1.0-2.0*c2)/c2)) * a
    tmp2=(mb1*((m2/m1)**n)+mb2*((m1/m2)**n)) * b
    tmp3=(mb1*(1.0-m1/mb1)+mb2*(1.0-m2/mb2)) * c

    meje_fit=np.maximum(tmp1+tmp2+tmp3+d,0)/1000.0
  
    return meje_fit

def calc_vrho(m1,c1,m2,c2):
    a=-0.219479
    b=0.444836 
    c=-2.67385      
  
    return ((m1/m2)*(1.0+c*c1)+(m2/m1)*(1.0+c*c2))*a+b

def calc_vz(m1,c1,m2,c2):
    a=-0.315585
    b=0.63808 
    c=-1.00757      

    return ((m1/m2)*(1.0+c*c1)+(m2/m1)*(1.0+c*c2))*a +b

def calc_vej(m1,c1,m2,c2):
    return np.sqrt(calc_vrho(m1,c1,m2,c2)**2.0+calc_vz(m1,c1,m2,c2)**2.0)

def calc_qej(m1,c1,m2,c2):
    vrho=calc_vrho(m1,c1,m2,c2)
    vz=calc_vz(m1,c1,m2,c2)
    vrho2=vrho*vrho
    vz2=vz*vz

    tmp1=3.*vz+np.sqrt(9*vz2+4*vrho2)
    qej=((2.0**(4.0/3.0))*vrho2+(2.*vrho2*tmp1)**(2.0/3.0))/((vrho**5.0)*tmp1)**(1.0/3.0)

    return qej

def calc_phej(m1,c1,m2,c2):
  return 4.0*calc_qej(m1,c1,m2,c2)*np.pi/2.0

def calc_lc(tini,tmax,dt,mej,vej,vmin,th,ph,kappa,eps,alp,eth,flgbct):

    td, bct = setbc_tabular()
    bc = setbc()

    t_d=[]
    lbol_d=[]
    bc_tmp=[]
    dc=0    

    t=np.max([tini,2.*(mej*100)**(1.0/3.2)])
    t = tini

    mag_d = {}
    for ii in xrange(8):
        mag_d[ii] = np.array([])

    epsBarnes = 0
    if epsBarnes:
        mejtab = (0.001,0.001,0.001,0.005,0.005,0.005,0.01,0.01,0.01,0.05,0.05,0.05)
        vejtab = (0.1,0.2,0.3,0.1,0.2,0.3,0.1,0.2,0.3,0.1,0.2,0.3)
        a      = (2.01,4.52,8.16,0.81,1.90,3.20,0.56,1.31,2.19,0.27,0.55,0.95)
        b      = (0.28,0.62,1.19,0.19,0.28,0.45,0.17,0.21,0.31,0.10,0.13,0.15)
        d      = (1.12,1.39,1.52,0.86,1.21,1.39,0.74,1.13,1.32,0.60,0.90,1.13) 

        fa     = scipy.interpolate.interp2d(mejtab,vejtab,a, kind='linear')
        fb     = scipy.interpolate.interp2d(mejtab,vejtab,b, kind='linear')
        fd     = scipy.interpolate.interp2d(mejtab,vejtab,d, kind='linear')

        fam    = fa(mej,vej)
        fbm    = fb(mej,vej)
        fdm    = fd(mej,vej)

    while t < tmax:
      t_d.append(t)

      if epsBarnes:
          eth = 0.36*(np.exp(-fam*t)+ np.log(1+2*fbm*t**fdm)/(2*fbm*t**fdm))[0] 

      lbol=kn_lbol(t,mej,vej,vmin,th,ph,kappa,eps,alp,eth)
      lbol_d.append(lbol)
      mbol=mag_bol(lbol,10)
      tt=t/((mej*100.0)**(1.0/3.2))
      bc_tmp=getBC(td,bc,bct,tt,flgbct)

      for ii in xrange(8):
        if t > 2.*(mej*100)**(1.0/3.2):
          mag_d[ii] = np.append(mag_d[ii],mbol-bc_tmp[ii])
        else:
          mag_d[ii] = np.append(mag_d[ii],np.nan)

      t=t+dt
    t_d = np.array(t_d)
    lbol_d = np.array(lbol_d)

    wavelengths = [3543, 4775.6, 6129.5, 7484.6, 8657.8, 12350, 16620, 21590]
    wavelength_interp = 9603.1

    mag_y = np.zeros(t_d.shape)
    for ii in xrange(len(t_d)):
        mags = [mag_d[jj][ii] for jj in xrange(8)]
        mag_y[ii] = np.interp(wavelength_interp,wavelengths,mags)
    mag_new = {}
    mag_new[0] = mag_d[0]
    mag_new[1] = mag_d[1]
    mag_new[2] = mag_d[2]
    mag_new[3] = mag_d[3]
    mag_new[4] = mag_d[4]
    mag_new[5] = mag_y
    mag_new[6] = mag_d[5]
    mag_new[7] = mag_d[6]
    mag_new[8] = mag_d[7]
  
    return t_d, lbol_d, mag_new

def mag_bol(lbol,d):
  f0=2.52e-5
  pc=3.08568e18
  d0=d*pc

  return -2.5*np.log(lbol/4/np.pi/d0/d0/f0)/np.log(10.0)

def getBC(td,bc,bct,tt,flgbct):

  if flgbct:
    if (tt<td[0]) or (tt>td[130]):
        return np.array([np.nan,np.nan,np.nan,np.nan,np.nan,np.nan,np.nan,np.nan])
    else:
      for ii in xrange(130):
          if (td[ii]<=tt) and (tt<=td[ii+1]):
              break
      bc_tmp=np.zeros((8,))
      fac=(tt-td[ii])/(td[ii+1]-td[ii])
      for jj in xrange(8):
          bc_tmp[jj]=(1-fac)*bct[jj][ii]+fac*bct[jj][ii+1]
          if not np.isfinite(bc_tmp[jj]):
              bc_tmp[jj] = np.nan
  else:
    if (tt<2) or (tt>15):
        return np.array([np.nan,np.nan,np.nan,np.nan,np.nan,np.nan,np.nan,np.nan])
    else:
      bc_tmp=np.zeros((8,))
      for jj in xrange(8):
          if (jj == 0) and (tt>5): 
            bc_tmp[jj]=np.nan
          elif (jj == 1) and (tt>8.5):
            bc_tmp[jj]=np.nan
          else:
            bc_tmp[jj]=bc[jj][0]+bc[jj][1]*tt+bc[jj][2]*(tt**2.0)+bc[jj][3]*(tt**3.0)+bc[jj][4]*(tt**4.0)
          if not np.isfinite(bc_tmp[jj]):
            bc_tmp[jj] = np.nan

  return bc_tmp

def kn_lbol(t,mej,vej,vmin,th,ph,kappa,eps,alp,eth):
  c=2.99792458e10
  msun=1.988e33
  day=24*3600
  lu0=c*day
  eneu0=msun*c*c
  lumu0=eneu0/day
  kappa0=kappa/lu0/lu0*msun
  eps0=eth*eps/eneu0*day*msun

  vdiff = vmax(vej,vmin)-vmin
  if vdiff < 0:
      tobs = 0.0
  else:
      tobs=(th*mej*kappa0/(2*ph*vdiff))**(1/2.0)

  if (t<tobs):
      fac=t/tobs
  else:
      fac=1

  lbol=(1+th)*mej*fac*eps0*(t**(-alp))*lumu0

  return lbol

def vmax(vej,vmin):
    return 2.0*vej-vmin 

